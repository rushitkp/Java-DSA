public class Operator2 {
    public static void main(String[] args) {
        int x = 200, y = 50, z = 100;

        if (x > y && x > y) {
            System.out.println("Hello");
        }
        if (z > x && z < x) {
            System.out.println("Java");
        }
        if ((y + 200) < x && (y + 200) > z) {
            System.out.println("Hello Java");
        }
    }
}
